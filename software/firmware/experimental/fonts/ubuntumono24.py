ifb = lambda l: l[0] | (l[1] << 8)


def get_ch(ch):
    oc = ord(ch)
    ioff = 2 * (oc - 32 + 1) if oc >= 32 and oc <= 126 else 0
    doff = ifb(_mvi[ioff:])
    width = ifb(_mvfont[doff:])

